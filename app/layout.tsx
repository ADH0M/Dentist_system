import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import StoreProvider from "@/store/store";
import { ThemeProvider } from "next-themes";
import Navbar from "@/components/ui/Navbar";
import { cookies } from "next/headers";
import { Footer } from "@/components/CopyrightFooter";
import { Toaster } from "@/components/ui/sonner";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const cookieStore = await cookies();
  const userId = cookieStore.get("userId")?.value;
  const username = cookieStore.get("username")?.value;
  const email = cookieStore.get("email")?.value;
  const role = cookieStore.get("role")?.value;
  const isActive = true;

  const user = userId ? { id: userId, username, email, role, isActive } : null;

  return (
    <StoreProvider>
      <html lang="en" suppressHydrationWarning>
        <body
          className={`${geistSans.variable} ${geistMono.variable} antialiased  main-bg`}
        >
          <ThemeProvider
            attribute="class"
            defaultTheme="system"
            disableTransitionOnChange
          >
            <Toaster />
            {/* <Sidebar /> */}
            <Navbar user={user} />
            <main className="h-[calc(100vh-66px)] overflow-y-auto">
              {children}
              <Footer />
            </main>
          </ThemeProvider>
        </body>
      </html>
    </StoreProvider>
  );
}
